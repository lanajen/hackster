  LAST_NAME_DISTRIBUTION = {
    last_name_none: 0.15,
    last_name_initial: 0.05,
    last_name_initial_dot: 0.05,
    last_name_full: 0.65,
  }
  USER_NAME_DISTRIBUTION = {
    user_name_full_name: 0.2,
    user_name_first_name: 0.1,
    user_name_initial_first_name: 0.3,
    user_name_initial_last_name: 0.3,
    user_name_none: 0.1,
  }

  attr_accessor :first_name, :last_name, :full_name, :user_name

  def self.sample
    new FIRST_NAMES.sample, LAST_NAMES.sample
  end

  def initialize first_name, last_name
    @first_name = first_name
    @last_name = last_name
  end

  def full_name
    return @full_name if @full_name

    methods = []
    LAST_NAME_DISTRIBUTION.each do |method, distrib|
      (distrib * 100).to_i.times { methods << method }
    end

    random_name = first_name + ' ' + send(methods.sample)
    @full_name = random_name.strip
  end

  def user_name
    return @user_name if @user_name

    methods = []
    USER_NAME_DISTRIBUTION.each do |method, distrib|
      (distrib * 100).to_i.times { methods << method }
    end

    @user_name = send methods.sample
  end

  private
    def cleanup text
      I18n.transliterate(text).downcase.gsub(/[^a-z]/, '')
    end

    def last_name_none
      ''
    end

    def last_name_initial
      last_name[0]
    end

    def last_name_initial_dot
      last_name[0] + '.'
    end

    def last_name_full
      last_name
    end

    def user_name_full_name
      cleanup full_name
    end

    def user_name_first_name
      cleanup first_name
    end

    def user_name_initial_first_name
      cleanup first_name[0] + last_name
    end

    def user_name_initial_last_name
      cleanup first_name + last_name[0]
    end

    def user_name_none
      nil
    end
end